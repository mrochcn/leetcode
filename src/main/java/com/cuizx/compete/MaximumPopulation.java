package com.cuizx.compete;

public class MaximumPopulation {
    public static void main(String[] args) {

    }

    public int maximumPopulation(int[][] logs) {
        int ans = 0;
        for (int i = 0; i < logs.length; i++) {
            for (int j = 0; j < logs[0].length; j++) {
                i++;
            }
        }
        return ans;
    }
}
